impl fmt::Display for Mnemonic {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        match self {
            Self::CondB(br) => f.write_fmt(format_args!("b.{:?}", br)),
            Self::yield_ => f.write_str("yield"),
            _ => fmt::Debug::fmt(self, f),
        }
    }
}
